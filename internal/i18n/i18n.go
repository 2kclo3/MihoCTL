package i18n

import (
	"fmt"
	"strings"
)

type Translator struct {
	lang string
	dict map[string]string
}

func New(lang string) *Translator {
	if _, ok := dictionaries[lang]; !ok {
		lang = "zh-CN"
	}
	return &Translator{
		lang: lang,
		dict: dictionaries[lang],
	}
}

func (t *Translator) T(key string) string {
	if value, ok := t.dict[key]; ok {
		return value
	}
	if value, ok := dictionaries["en-US"][key]; ok {
		return value
	}
	return key
}

func (t *Translator) Tf(key string, data map[string]any) string {
	text := t.T(key)
	for k, v := range data {
		text = strings.ReplaceAll(text, "{"+k+"}", fmt.Sprint(v))
	}
	return text
}

